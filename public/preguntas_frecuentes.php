    <h1>Preguntas Frecuentes</h1>
    <p>Encuentra aquí las respuestas a las dudas más comunes sobre nuestras marcas y servicios.</p>

    <h2>¿Los productos son originales?</h2>
    <p>Sí. En El Corte Rebelde solo vendemos artículos 100% originales y certificados. Trabajamos exclusivamente con distribuidores oficiales y proveedores autorizados de marcas de lujo.</p>

    <h2>¿Los productos son nuevos o de segunda mano?</h2>
    <p>Todos nuestros productos son completamente nuevos, de primera mano y en su embalaje original. No vendemos artículos usados ni reacondicionados.</p>

    <h2>¿Qué marcas ofrecéis?</h2>
    <p>Disponemos de una selección de firmas de lujo como Gucci, Louis Vuitton, Balenciaga, Dior, Prada, Off-White, Burberry y otras marcas premium reconocidas internacionalmente.</p>

    <h2>¿Hacéis envíos a toda España?</h2>
    <p>Sí, realizamos envíos a cualquier punto de España. También ofrecemos envío express en determinadas zonas.</p>

    <h2>¿Realizáis envíos internacionales?</h2>
    <p>Sí, enviamos a varios países de la Unión Europea. Para envíos fuera de Europa, puedes consultarnos directamente.</p>

    <h2>¿Cuánto tarda en llegar mi pedido?</h2>
    <p>Los envíos dentro de España suelen tardar entre 24 y 72 horas laborables. Los envíos internacionales pueden tardar entre 3 y 7 días.</p>

    <h2>¿Puedo devolver un producto?</h2>
    <p>Sí, aceptamos devoluciones dentro de los primeros 14 días desde la recepción del pedido, siempre que el artículo esté sin usar, con etiquetas y en su embalaje original.</p>

    <h2>¿Los productos incluyen factura o certificado?</h2>
    <p>Sí. Todos los artículos se envían con su factura correspondiente y, cuando la marca lo incluye, con tarjeta de autenticidad o documentación oficial.</p>

    <h2>¿Cómo puedo contactar con vosotros?</h2>
    <p>Puedes escribirnos a <strong>contacto@elcorterebelde.com</strong> o utilizar el formulario de contacto de nuestra web.</p>

    <h2>¿Tenéis tienda física?</h2>
    <p>Actualmente operamos online, pero estamos trabajando en abrir un showroom privado para clientes que buscan una experiencia más exclusiva.</p>

